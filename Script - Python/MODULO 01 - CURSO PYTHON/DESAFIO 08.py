n1 = int(input('Digite um número: '))
print('O dobro é {}, o tripo é {} e a sua raiz quadrada é {}, do número {}'.format(n1 * 2, n1 * 3, n1**(1/2), n1))
# Se lembre que para saber a raiz, use ** com a divisão, por exemplo:
# ou X ** (1/2) ou X ** (1/3)
# Uma é raiz quadrada e outra é raiz cúbica/terceira